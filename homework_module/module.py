def square_footage(length, width):
    """this function takes 2 arguments,length and width, to find the area of a house."""
    area = length * width
    print("The area is {}".format(area))


def circum_circle(radius):
    from math import pi
    """this function takes 1 variable, radius, to find the circumference of a circle."""
    circum = 2 * pi * radius
    print("Sir Cumference is {}, as round as the Round Table.".format(circum))
